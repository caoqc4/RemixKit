export function statusLabel(language: DashboardLanguage, status: "completed" | "running" | "failed" | "queued") {
  const t = text[language]
  return {
    completed: t.completed,
    running: t.running,
    failed: t.failed,
    queued: t.queued,
  }[status]
}

export function metricLabel(language: DashboardLanguage, label: string) {
  const t = text[language]
  const labels: Record<string, string> = {
    "生成变体": t.metricVariants,
    "处理时长": t.metricDuration,
    "镜头证据": t.metricFrames,
    "转写片段": t.metricTranscript,
  }
  return labels[label] ?? label
}

export function artifactName(language: DashboardLanguage, name: string) {
  const t = text[language]
  const names: Record<string, string> = {
    "镜头证据": t.artifactFacts,
    "创意解读": t.artifactAnalysis,
    "变体方案": t.artifactBrief,
  }
  return names[name] ?? name
}
